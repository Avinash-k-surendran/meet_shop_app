class CartProvider extends ChangeNotifier {
  var adidasItems = Fishes;
  List<ProductModel> get adidasItemsHome => adidasItems;

  List<ProductModel> cart = [];
  List<ProductModel> get cartItems => cart;

  void addToCart(ProductModel data) {
    int index = cart.indexWhere((element) => element == data);
    if (index != -1) {
      cart[index].count = cart[index].count + 1;
    } else {
      cart.add(data);
    }
    notifyListeners();
  }

  void removeFromCart(ProductModel data){
    int index = cart.indexWhere((element) => element == data);
    if (index != -1) {
      cart[index].count = cart[index].count + 1;
    } else {
      cart.remove(data);
    }
    notifyListeners();
  }
}